/****** Script for SelectTopNRows command from SSMS  ******/


-- NOT GROUP 
SELECT TOP (1000) [ID]
      ,[LASTNAME]
      ,[FIRSTNAME]
      ,[NORMALPAY]
      ,[OVERTIME]
      ,[NETTPAY]
      ,CONVERT(VARCHAR(7),[PAYDATE],120) [PAYDATE]
  FROM [UIF_DB].[dbo].[CL_DATA]


--- GROUPED BY MONTH
SELECT TOP (1000) [ID]
      ,[LASTNAME]
      ,[FIRSTNAME]
      ,SUM([NORMALPAY]) [NORMALPAY]
      ,SUM([OVERTIME]) [OVERTIME]
      ,SUM([NETTPAY]) [NETTPAY]
      ,CONVERT(VARCHAR(7),[PAYDATE],120) [PAYDATE]
  FROM [UIF_DB].[dbo].[CL_DATA]
  GROUP BY CONVERT(VARCHAR(7),[PAYDATE],120),[ID], [LASTNAME],[FIRSTNAME]
  ORDER BY LASTNAME